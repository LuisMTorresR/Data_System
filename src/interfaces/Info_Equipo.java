    public Info_Equipo() {
        initComponents();
        this.setLocationRelativeTo(null);
        setTitle("Usuario " + Login.user + ".");
        ImageIcon wallpaper = new ImageIcon("src/images/wallpaperPrincipal.jpg");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(label_wallpaper.getWidth(),
                label_wallpaper.getHeight(), Image.SCALE_DEFAULT));
        label_wallpaper.setIcon(icono);

        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select * from equipos where id_equipo = " + Info_Cliente.equipo2);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {

                txt_id.setText(rs.getString("id_equipo"));
                combo_tipo_equipo.setSelectedItem(rs.getString("tipo_equipo"));
                combo_marca_equipo.setSelectedItem(rs.getString("marca"));
                txt_modelo_equipo.setText(rs.getString("modelo"));
                txt_num_serie.setText(rs.getString("num_serie"));
                txt_fecha.setText(rs.getString("dia_ingreso") + ("/") + rs.getString("mes_ingreso") + ("/") + rs.getString("annio_ingreso"));
                combo_status_equipo.setSelectedItem(rs.getString("status"));
                txt_ultima_modificacion.setText(rs.getString("ultima_modificacion"));
                jTextArea_observaciones.setText(rs.getString("observaciones"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al cargar la informacion");

        }

    }

