    private void boton_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_ActualizarActionPerformed

        try {
            Connection cn = Conexion.conectar();

            PreparedStatement pst = cn.prepareStatement("update equipos set tipo_equipo = ?, marca = ?, modelo = ?, num_serie = ?, "
                    + "observaciones = ? status = ?, ultima_modificacion = ? where id_equipo = " + Info_Cliente.equipo2);

            pst.setString(1, (String) combo_tipo_equipo.getSelectedItem());
            pst.setString(2, (String) combo_marca_equipo.getSelectedItem());
            pst.setString(3, txt_modelo_equipo.getText().trim());
            pst.setString(4, txt_num_serie.getText().trim());
            pst.setString(5, jTextArea_observaciones.getText());
            pst.setString(6, (String) combo_status_equipo.getSelectedItem());
            pst.setString(7, Login.user);
            pst.executeUpdate();
            txt_ultima_modificacion.setText(Login.user);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al actualizar");
        }

    }//GEN-LAST:event_boton_ActualizarActionPerformed

