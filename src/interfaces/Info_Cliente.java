    public Info_Cliente() {
        initComponents();
        this.setLocationRelativeTo(null);
        setTitle("Usuario " + Login.user + ".");
        ImageIcon wallpaper = new ImageIcon("src/images/wallpaperPrincipal.jpg");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(label_wallpaper.getWidth(),
                label_wallpaper.getHeight(), Image.SCALE_DEFAULT));
        label_wallpaper.setIcon(icono);

        String datos[] = new String[4];

        tabla.addColumn("ID");
        tabla.addColumn("Tipo");
        tabla.addColumn("Marca");
        tabla.addColumn("Status");
        tabla.setRowCount(0);

        //llenado de tabla_equipos
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select * from equipos where id_cliente = " + Gestion_Cliente.cliente);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {

                datos[0] = rs.getString("id_equipo");
                datos[1] = rs.getString("tipo_equipo");
                datos[2] = rs.getString("marca");
                datos[3] = rs.getString("status");
                tabla.addRow(datos);
            }
            tabla_equipos.setModel(tabla);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en la tabla de Info_Cliente ");
        }

        //llenado de la info_cliente
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select * from clientes where id_cliente = '" + Gestion_Cliente.cliente + " '");
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {

                txt_nombre_cliente.setText(rs.getString("nombre_cliente"));
                txt_email_cliente.setText(rs.getString("email_cliente"));
                txt_telefono_cliente.setText(rs.getString("tel_cliente"));
                txt_direccion_cliente.setText(rs.getString("dir_cliente"));
                txt_ultima_modificacion.setText(rs.getString("ultima_modificacion"));

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en llenado de la info del Cliente ");
        }

    }

