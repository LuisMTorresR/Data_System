    private void jButton_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ActualizarActionPerformed

        //actualizacon de datos
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("update clientes set nombre_cliente = ?, email_cliente = ?, tel_cliente = ?, dir_cliente = ?, ultima_modificacion = ? where id_cliente = " + Gestion_Cliente.cliente);

            pst.setString(1, txt_nombre_cliente.getText().trim());
            pst.setString(2, txt_email_cliente.getText().trim());
            pst.setString(3, txt_telefono_cliente.getText().trim());
            pst.setString(4, txt_direccion_cliente.getText().trim());
            pst.setString(5, Login.user);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Informacion actualizada");

            txt_ultima_modificacion.setText(Login.user);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en la actualizacion de datos del cliente");
        }

    }//GEN-LAST:event_jButton_ActualizarActionPerformed

