    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (txt_nombre_usuario.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos!");
        } else if (txt_mail_usuario.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos!");
        } else if (txt_telefono_usuario.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos!");
        } else if (txt_username.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos!");
        } else if (password.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos!");
        } else if (combo_seleccion_permiso.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(null, "Debes seleccionar el nivel de acceso!");
        } else {
            try {
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement("insert into usuarios values(?,?,?,?,?,?,?,?,?)");

                pst.setString(1, "0");
                pst.setString(2, txt_nombre_usuario.getText().trim());
                pst.setString(3, txt_mail_usuario.getText().trim());
                pst.setString(4, txt_telefono_usuario.getText().trim());
                pst.setString(5, txt_username.getText().trim());
                pst.setString(6, password.getText().trim());
                pst.setString(7, (String) combo_seleccion_permiso.getSelectedItem());
                pst.setString(8, "Activo");
                pst.setString(9, Login.user);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Registro Exitoso");

                txt_nombre_usuario.setText("");
                txt_mail_usuario.setText("");
                txt_telefono_usuario.setText("");
                txt_username.setText("");
                password.setText("");
                combo_seleccion_permiso.setSelectedItem(" ");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al registrar usuario");
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

